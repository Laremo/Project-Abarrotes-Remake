    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
        BufferedReader reader = request.getReader();
        Gson gson = new Gson();
        java.lang.reflect.Type cartType = new TypeToken<Map<String, Map<String, Object>>>() {
        }.getType();
        Map<String, Map<String, Object>> cartMap = gson.fromJson(reader, cartType); // Create and populate the Carrito object 
        Carrito carrito = new Carrito();
        carrito.mapearProductos(cartMap); // Save the cart 
        carrito.saveCart(); // Send a response 
        response.setContentType("application/json");
        response.getWriter().write("{\"status\":\"success\"}");
    }

